     */
    public function index()
    {
        //
        $tags = DB::table('tags')
            ->select('tagname', DB::raw('count(*) as count'))
            ->groupBy('tagname')
            ->orderBy('tagname', 'asc')
            ->get();
        return view('taglist', ['tags' => $tags]);
    }

